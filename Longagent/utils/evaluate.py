def extract_answer(text):
    answer = re.findall(r'\d+', text)
    if len(answer) == 0:
        return 0
    else:
        return int(answer[0])

def passkey_retrieval_accuracy(predictions, labels):
    predictions = [extract_answer(pred) for pred in predictions]
    if len(predictions) > 0:
        accuracy = sum(pred == ref for pred, ref in zip(predictions, labels)
                   ) / len(predictions)
    else:
        accuracy = 0.0  # 处理预测为空的情况
    return {
        "accuracy": accuracy
    }
    
